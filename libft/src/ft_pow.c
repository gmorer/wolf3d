#include "libft.h"

int		ft_pow(int x, int y)
{
	int	rslt;

	rslt = 1;
	if (!(y))
		return (1);
	if (y < 0)
		return (0);
	y = ft_abs(y);
	while (y--)
		rslt = rslt * x;
	return (rslt);
}
